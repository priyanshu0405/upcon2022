<h2>Instructions for preparing manuscripts</h2>
</div>
</div>
<div class="row justify-content-left mb-5">
  <p>
    Prospective authors are invited to submit full-length original research papers. In submitting a manuscript to CVIP-2020, authors acknowledge that no paper substantially similar in content has been or will be submitted to another journal, conference or workshop during the review period. In such a case the paper will be rejected without review. Papers must be electronically submitted before the deadline expires without exception.<br>
    The paper must not exceed 12 pages in length (including all text, figures, and references).
    The manuscript must be submitted in pdf format only and the file size of your manuscript should not exceed 10 MB.<br>
    Use a proper tool to convert the resulting source into a pdf document that has only scalable fonts with all fonts embedded.<br>
    The images embedded in the paper must not contain transparent pixels (i.e., an alpha-channel of a transparent color) since this could lead to problems when displaying or printing the pdf.<br>
    The pdf manuscript must not have Adobe Document Protection or Document Security enabled.<br><br>
    <b>Proceedings: </b><br>
    The proceedings of the conference are published by Scopus Indexed - Springer, <a href="http://www.springer.com/series/7899">Communications in Computer and Information Science series</a>. CCIS is abstracted/indexed in DBLP, Google Scholar, EI-Compendex, Mathematical Reviews, SCImago, Scopus. CCIS volumes are also submitted for the inclusion in ISI Proceedings.<br><br>
    <b>Preparing your proceedings paper:</b><br>
    For your convenience, we have summarized in the "Author Guidelines" document how a proceedings paper should be structured, how elements (headings, figures, references) should be formatted using our predefined styles, etc. We also give some insight on how your paper will be typeset at CVIP. The PDF of the Authors Guidelines can be downloaded here<br>
    <a href="https://www.springer.com/gp/authors-editors/conference-proceedings/conference-proceedings-guidelines">Proceedings Guidelines for Authors</a><br><br>
    Authors must use the manuscript template specified here. The LaTeX and Word templates can be downloaded from the following links:<br>
    <a href="/files/splnproc1703_cvip2020.zip">Word Template</a><br>
    <a href="/files/llncs2e_cvip2020.zip">LaTeX Template</a><br><br>
    <b>Instruction for uploading manuscripts:</b><br>
    The manuscript has to be uploaded online at the CVIP-2020 Microsoft Research paper submission portal at the following link:<br>
    <a href="https://cmt3.research.microsoft.com/CVIP2020">https://cmt3.research.microsoft.com/CVIP2020</a>
  </p>
